// push add element of the last of array
let numbers = [10, 20, 30];
numbers.push(40);
console.log(numbers);
// pop remove element of the last of array
numbers = [10, 20, 30, 50];
console.log(numbers);
numbers.pop();
console.log(numbers);
// shift delete the start array items
let xyz = [10, 50, 60, 66, 100];
xyz.shift();
console.log(xyz);
// unshift add numbers to the start
xyz = [30, 33, 40, 60];
console.log(xyz);
xyz.unshift(10);
console.log(xyz);