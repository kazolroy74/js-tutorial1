//for loop in array
let fruits = ["apple", "banana", "cherry"];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

let xyx = ["rahim", "karim", "sagor", "mahim"];
for (let i = 0; i < xyx.length; i++) {
    console.log(xyx[i]);
}

//while loop
let i = 0;

while (i < fruits.length) {
    console.log(fruits[i]);
    i++;
}